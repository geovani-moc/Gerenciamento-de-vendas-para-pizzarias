    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        if (cbEscolhaBusca.getSelectedItem().toString().equals("Cliente")) {
            JTCliente.setModel(modelCliente);
            if (!txtBuscar.getText().equals("")) {
                modelCliente.setList(cl_d.buscar(txtBuscar.getText(), 8));
                JTCliente.repaint();
            } else {
                modelCliente.setList(new ArrayList<Cliente>());
                JTCliente.repaint();
            }
        } else {
            JTCliente.setModel(modelFuncionario);
            if (!txtBuscar.getText().equals("")) {
                modelFuncionario.setList(fu_d.buscar(txtBuscar.getText(), 8));
                JTCliente.repaint();
            } else {
                modelFuncionario.setList(new ArrayList<Funcionario>());
                JTCliente.repaint();
            }
        }
    }//GEN-LAST:event_txtBuscarKeyReleased

