    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (checkFun.isSelected()) {
            FuncionarioDao fu_d = new FuncionarioDao();
            Funcionario f = new Funcionario();
            f.setNome(txtNome.getText());
            f.setCpf(Long.parseLong(txtCpf.getText()));
            f.setTelefone(Long.parseLong(txtTelefone.getText()));
            f.setBairro(txtBairro.getText());
            f.setComplemento(txtComplemto.getText());
            f.setNumero(Integer.parseInt(txtNumero.getText()));
            f.setRua(txtRua.getText());
            f.setCargo(cbFun.getSelectedItem().toString());

            fu_d.salvar(f);
        } else {

            ClienteDao cl_d = new ClienteDao();
            Cliente c = new Cliente();
            c.setNome(txtNome.getText());
            c.setCpf(Long.parseLong(txtCpf.getText()));
            c.setTelefone(Long.parseLong(txtTelefone.getText()));
            c.setBairro(txtBairro.getText());
            c.setComplemento(txtComplemto.getText());
            c.setNumero(Integer.parseInt(txtNumero.getText()));
            c.setRua(txtRua.getText());
            c.setAtivo(true);

            cl_d.salvar(c);
        }
        txtNome.setText("");
        txtCpf.setText("");
        txtTelefone.setText("");
        txtBairro.setText("");
        txtComplemto.setText("");
        txtNumero.setText("");
        txtRua.setText("");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (JTCliente.getSelectedRow() != -1) {
            if (cbEscolhaBusca.getSelectedItem().toString().equals("Cliente")) {
                cl_d.excluir(modelCliente.getCliente(JTCliente.getSelectedRow()));
                modelCliente.setList(cl_d.buscar(txtBuscar.getText(), 8));
                JTCliente.repaint();
            } else {
                fu_d.excluir(modelFuncionario.getFuncionario(JTCliente.getSelectedRow()));
                modelFuncionario.setList(fu_d.buscar(txtBuscar.getText(), 8));
                JTCliente.repaint();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

